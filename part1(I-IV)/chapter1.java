
// 1. **Java is Object-Oriented:**
//    - Java is based on objects and classes. Objects are instances of classes and encapsulate data and behavior.




// Advantages of Object-Oriented Programming (OOP)
// Modularity:

// Code is organized into classes and objects, making it modular and easier to manage, maintain, and understand.
// Reusability:

// Objects and classes can be reused across different programs without modification, enhancing code reusability.
// Encapsulation:

// Data and methods are encapsulated within objects, which helps in protecting the internal state of an object and preventing unintended interference and misuse.
// Abstraction:

// OOP allows the creation of complex systems by modeling real-world entities as objects. This abstraction simplifies problem-solving by focusing on high-level design.
// Inheritance:

// Inheritance allows new classes to inherit properties and behavior from existing classes, promoting code reuse and reducing redundancy.
// Polymorphism:

// Polymorphism enables objects to be treated as instances of their parent class, allowing for dynamic method invocation and more flexible and scalable code.
// Scalability:

// OOP makes it easier to manage and expand complex systems by adding new objects and classes without affecting existing code.





// 2. **Writing and Running Java:**
//    - Java code is written in `.java` files.
//    - The Java compiler (`javac`) compiles the `.java` files into bytecode (`.class` files).
//    - The Java Virtual Machine (JVM) executes the bytecode.

// 3. **Basic Structure of a Java Program:**
//    - A typical Java program consists of a class definition with a `main` method.
//    - Example:
     
    public class chapter1 {
         public static void main(String[] args) {
             System.out.println("I Rule!");
             System.out.println("The World");
         }
     }


// 4. **The `main` Method:**
//    - The `main` method is the entry point of a Java application.
//    - It has the signature `public static void main(String[] args)`.

// 5. **Compiling and Running:**
//    - Compile the Java file using `javac MyFirstApp.java`.
//    - Run the compiled bytecode using `java MyFirstApp`.

// 6. **Comments:**
//    - Single-line comments use `//`.
//    - Multi-line comments use `/* */`.

// 7. **Basic Output:**
//    - `System.out.println` is used for printing text to the console.

// 8. **The Anatomy of a Class:**
//    - A class is defined with the `class` keyword followed by the class name.
//    - A class contains fields (variables) and methods (functions).

// 9. **Creating and Using Objects:**
//    - Objects are created using the `new` keyword.
//    - Example:
     
    Dog myDog = new Dog();
  

// 10. **Primitive Data Types:**
//     - Java has eight primitive data types: `byte`, `short`, `int`, `long`, `float`, `double`, `char`, and `boolean`.

// 11. **Methods:**
//     - Methods define the behavior of objects.
//     - Example:
     
      class Dog {
          void bark() {
              System.out.println("Woof!");
          }
      }
     

// 12. **Class vs. Object:**
//     - A class is a blueprint for creating objects.
//     - An object is an instance of a class.

// 13. **Encapsulation:**
//     - Encapsulation is the concept of wrapping data and methods within a class and restricting access using access modifiers like `private` and `public`.

// 14. **Inheritance:**
//     - Java supports inheritance, where a class can inherit fields and methods from another class using the `extends` keyword.

// 15. **Polymorphism:**
//     - Polymorphism allows methods to do different things based on the object it is acting upon.

